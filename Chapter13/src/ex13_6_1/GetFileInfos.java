//章节13.6.1                  获取文件信息示例
package ex13_6_1;

import java.io.File;
import java.util.Date;

/*
 * 获取文件的基本信息
 */

public class GetFileInfos {
	
	public static void println(String s) {
		System.out.println(s);
	}

	public static void main(String[] args) {
		//用文件路径新建一个文件对象。路径可以是绝对路径，也可以是相对路径
		//传入的参数被当做文件的抽象路径
		File file=new File("F:/MyWorkspace/JavaLearn/JavaCode_Learn/Chapter13/src/ex13_6_1/GetFileInfos.java");
		println("文件名：\t"+file.getName());         //获取文件的名字，不包括路径
		//将抽象路径命中的文件分隔符用系统默认分隔符替换
		println("文件路径：\t"+file.getPath());
		
		println("绝对路径：\t"+file.getAbsolutePath());               //获取文件的绝对路径
		println("父目录：\t"+file.getParent());               //获取抽象路径名的父抽象路径
		println("文件是否存在：\t"+file.exists());
		println("是否可读：\t"+file.canRead());
		println("是否可写：\t"+file.canWrite());
		println("是否是隐藏文件：\t"+file.isHidden());
		println("是否是普通文件：\t"+file.isFile());
		println("是否是文件目录：\t"+file.isDirectory());
		println("文件路径是否是绝对路径："+file.isAbsolute());
		println("文件路径的URI：\t"+file.toURI());
		println("文件最后修改时间：\t"+new Date(file.lastModified()));
		println("文件大小：\t"+file.length()+" bytes");
	}

}
